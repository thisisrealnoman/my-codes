var color = 'white';

switch(color){
    case 'black':
        console.log("you're my black friend!");
        break;
    case 'blue':
        console.log("you're my blue friend!");
        break;
    case 'red':
        console.log("you're my red friend!");
        break;
    case 'yellow':
        console.log("you're my yellow friend!");
        break;
    case 'white':
        console.log("you're my white friend!");
        break;
    default:
        console.log("Invalid option!");
    
}